%%%-------------------------------------------------------------------
%% @doc fuge top level supervisor.
%% @end
%%%-------------------------------------------------------------------

-module(fuge_sup).

-behaviour(supervisor).

%% API
-export([start_link/0]).

%% Supervisor callbacks
-export([init/1]).

-define(SERVER, ?MODULE).

%%-------------------------------------------------------------------
%% API functions
%%-------------------------------------------------------------------

start_link() ->
    supervisor:start_link({local, ?SERVER}, ?MODULE, []).

%%-------------------------------------------------------------------
%% Supervisor callbacks
%%-------------------------------------------------------------------

init([]) ->
    {ok, {{one_for_all, 0, 1}, [server()]}}.

%%-------------------------------------------------------------------
%% Internal functions
%%-------------------------------------------------------------------

server() ->
    {fuge_server, {fuge_server, start_link, []},
     temporary, 5000, worker, []}.
